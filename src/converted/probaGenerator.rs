unsafe extern "C" fn GEN_rand(mut seed: *mut libc::c_uint) -> libc::c_uint {
    *seed = (*seed)
        .wrapping_mul(2654435761u32)
        .wrapping_add(2246822519u32);
    return *seed >> 11i32;
}
unsafe extern "C" fn usage() -> libc::c_int {
    fprintf(
        __stderrp,
        b"Usage :\n\x00" as *const u8 as *const libc::c_char,
    );
    fprintf(
        __stderrp,
        b"%s P%%\n\x00" as *const u8 as *const libc::c_char,
        g_programName,
    );
    fprintf(
        __stderrp,
        b"Exemple :\n\x00" as *const u8 as *const libc::c_char,
    );
    fprintf(
        __stderrp,
        b"%s 70%%\n\x00" as *const u8 as *const libc::c_char,
        g_programName,
    );
    return 0i32;
}
unsafe extern "C" fn badusage() -> libc::c_int {
    if displayLevel >= 1i32 {
        fprintf(
            __stderrp,
            b"Incorrect parameters\n\x00" as *const u8 as *const libc::c_char,
        );
    }
    if displayLevel >= 1i32 {
        usage();
    }
    fprintf(
        __stderrp,
        b"Press enter to exit \n\x00" as *const u8 as *const libc::c_char,
    );
    getchar();
    exit(1i32);
}
